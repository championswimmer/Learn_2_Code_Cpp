#include <iostream>

using namespace std;

int main() {
    int a = 10;
    int b = 20;

    cout << a + b << endl;
    cout << a - b << endl;
    cout << a * b << endl;
    cout << a / b << endl;
    cout << b / a << endl;
    cout << a % b << endl;

    cout << a++ << endl;
    cout << ++b << endl;

    cout << a << endl;
    cout << b << endl;
}
